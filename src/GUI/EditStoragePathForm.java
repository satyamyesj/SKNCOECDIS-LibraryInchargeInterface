    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        this.setAlwaysOnTop(false);
        if (!(storagePathText.getText().equals(""))) {
            String path = storagePathText.getText().trim();
            storagePath.path = path;
            storagePath.path = storagePath.parseStringToPath(storagePath.path);
            if (storagePath.path.equals("INVALID_PATH")) {
                JOptionPane.showMessageDialog(null, "invalid path");
            } else {
                storagePath = StoragePath.saveStoragePath(storagePath);
                if (!storagePath.path.equals("PATH_NOT_SAVED") && !storagePath.path.equals("PATH_NOT_FOUND")) {
                    GUI.libraryInchargeInterfaceFrame.setEnabled(true);
                    GUI.libraryInchargeInterfacePanel.showRequestResult("storage path updated successfully");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "storage path not saved");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "storage path required to save details");
        }
        this.setAlwaysOnTop(true);
    }//GEN-LAST:event_saveButtonActionPerformed


    private void browseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseButtonActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
         jFileChooser.setCurrentDirectory(new java.io.File(".")); // start at application current directory
        jFileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int returnValue=  jFileChooser.showSaveDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectFolder = jFileChooser.getSelectedFile();
          this.storagePathText.setText(selectFolder.getAbsolutePath());
        }
    }//GEN-LAST:event_browseButtonActionPerformed

